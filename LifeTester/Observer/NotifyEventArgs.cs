using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace LifeTester.Observer
{
    /// <summary>
    /// 提供消息发送事件参数
    /// </summary>
    public class NotifyEventArgs : EventArgs
    {
        /// <summary>
        /// 获取或设置通知消息类型
        /// </summary>
        public MessageTypes MessageTypes { get; set; }
        /// <summary>
        /// 获取或设置通知时传递的参数对象
        /// </summary>
        public object ParamObject { get; set; }
    }
}
